package chapter5;

public class basics {

	public static void main(String[] args) 
	{
		// using for loop, repetition
		
		for(int i=5;i<=50;i+=5)
		{
			System.out.print(i+" ");
			}
		System.out.println();
		// by using while loop
		int j=0;
		while(j<50)
		{
			j+=5;
			System.out.print(j+" ");
		}
	}

}
